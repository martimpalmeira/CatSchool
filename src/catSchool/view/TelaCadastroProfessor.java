    private void salvarProfessor(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarProfessor
        String nome = jTextFieldNome.getText();
        String cpf = jTextFieldCpf.getText();
        String formacao = jTextFieldFormacao.getText();
        String identidadeProfissional = jTextFieldIdentidadeProfissional.getText();
        
        try {
            boolean conseguiuCadastrarProfessor = ProfessorController.cadastrarProfessor(nome,cpf,formacao,identidadeProfissional);
            if(conseguiuCadastrarProfessor){
            JOptionPane.showMessageDialog(null, "Professor cadastrado com sucesso ");
            this.limparTelaCadastroProfessor(evt);
            }else{
             JOptionPane.showMessageDialog(null, "Dados não foram inseridos corretamente");

            }
                
        } catch (ExceptionDAO ex) {
            JOptionPane.showMessageDialog(null, "Erro: "+ ex);
        }
                

    }//GEN-LAST:event_salvarProfessor

