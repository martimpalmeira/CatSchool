    private void apagarProfessor(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_apagarProfessor
        boolean conseguiuApagarProfessor;
        ProfessorController professorController = new ProfessorController();
        try{
            conseguiuApagarProfessor = professorController.apagarProfessor(this.nome);
            if(conseguiuApagarProfessor){
               JOptionPane.showMessageDialog(null, "O professor foi apagado com sucesso");
               this.limparTelaCadastroProfessor(evt);
            }else{
                JOptionPane.showMessageDialog(null, "O professor não foi apagado. Por favor selecione um aluno");
            }
        }catch(Exception e){
            
            JOptionPane.showMessageDialog(null, "Erro: "+e);

        }
    }//GEN-LAST:event_apagarProfessor

